class Qwen3_5MoeConfig(PreTrainedConfig):
    r"""
    Top-level Qwen3.5-MoE config.

    NOTE for tests: the upstream class is a thin wrapper that only carries
    `text_config` and `vision_config` sub-configs. To make block-level tests
    convenient (constructing the GatedDeltaNet / SparseMoeBlock directly), the
    patched class transparently forwards text-side kwargs (`hidden_size`,
    `num_experts`, `linear_*`, etc.) to a synthesized `Qwen3_5MoeTextConfig`
    and exposes the same attributes on itself.
    """

    model_type = "qwen3_5_moe"
    sub_configs = {"vision_config": Qwen3_5MoeVisionConfig, "text_config": Qwen3_5MoeTextConfig}
    keys_to_ignore_at_inference = ["past_key_values"]

    def __init__(
        self,
        text_config=None,
        vision_config=None,
        image_token_id: int = 248056,
        video_token_id: int = 248057,
        vision_start_token_id: int = 248053,
        vision_end_token_id: int = 248054,
        tie_word_embeddings: bool = False,
        **kwargs,
    ):
        # Separate kwargs intended for the text config so that block tests that
        # pass e.g. `Qwen3_5MoeConfig(hidden_size=64, ...)` still work.
        text_field_names = set(Qwen3_5MoeTextConfig.__init__.__code__.co_varnames)
        text_kwargs = {k: kwargs.pop(k) for k in list(kwargs) if k in text_field_names}

        if isinstance(vision_config, dict):
            vision_config = Qwen3_5MoeVisionConfig(**vision_config)
        elif vision_config is None:
            vision_config = Qwen3_5MoeVisionConfig()
        self.vision_config = vision_config

        if isinstance(text_config, dict):
            merged = {**text_config, **text_kwargs}
            text_config = Qwen3_5MoeTextConfig(**merged)
        elif text_config is None:
            text_config = Qwen3_5MoeTextConfig(**text_kwargs)
        self.text_config = text_config

        # Mirror every text-config attribute onto self for the benefit of code
        # that does `config.hidden_size` etc. on the top-level config.
